# regular division turning integers into floats
print(8/3)

# round a division
print(round(8/3))

# round a division to a decimal place
print(round(8/3, 2))

# round a float
print(round(2.6666, 2))

# convert to integer
print(8 // 3)

# set variable and divide with the /= operator
result = 4/2
result /= 2
print(result)

# increment increase shorthand
score = 0
score += 1
print(score)

# increment decrease shorthand
score1 = 0
score1 -= 1
print(score1)

